package Arrays;

public class BAsicsArray {
    public static void main(String[] args) {
        int[] arr= new int[5];

        arr[0]=100;
        arr[1]=90;
        arr[2]=80;
        arr[4]=70;

        System.out.println(arr[0]);
        System.out.println(arr[1]);
        System.out.println(arr[2]);
        System.out.println(arr[4]);
    }
}
